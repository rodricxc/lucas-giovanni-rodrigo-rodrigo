    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       
        SelecionarProfessor o;
        o = new SelecionarProfessor(null, true);
        o.setVisible(true);
        Professor a = o.getProf();
        if(a!=null){
            this.nomeProfessor.setText(a.getNome());
            this.cpfProfessor.setText(a.getCpf());
            this.nomeProfessor.setEnabled(true);
            this.cpfProfessor.setEnabled(true);
            this.professor = a;
        }else{
            JOptionPane.showMessageDialog(null,"Nenhum Professor selecionado!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

