    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        AlunoDao aDao = AlunoDaoImpl.getInstance();
        Aluno a = new Aluno(jTextField1.getText(),jTextField2.getText());
        if(aDao.getCPF(a.getCpf()).isEmpty()){
            aDao.add(a);
            mensagemAdicionado(a);
            jTextField1.setText("");
            jTextField2.setText("");
            
        }else if((!a.getCpf().equals(""))&&(!a.getNome().equals(""))){
            mensagemNaoAdicionado(a);
        }else {
            mensagemNaoAdicionadoValorInvalido(a);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

